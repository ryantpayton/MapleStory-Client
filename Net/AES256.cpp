	void AES256::encrypt(uint8_t* bytes) const
	{
		uint8_t round = 0;
		addroundkey(bytes, round);

		for (round = 1; round < 14; round++)
		{
			subbytes(bytes);
			shiftrows(bytes);
			mixcolumns(bytes);
			addroundkey(bytes, round);
		}

		subbytes(bytes);
		shiftrows(bytes);
		addroundkey(bytes, round);
	}

	void AES256::addroundkey(uint8_t* bytes, uint8_t round) const
	{
		uint8_t offset = round * 16;
		for (uint8_t i = 0; i < 16; i++)
		{
			bytes[i] ^= maplekey[i + offset];
		}
	}

	void AES256::subbytes(uint8_t* bytes) const
	{
		for (uint8_t i = 0; i < 16; i++)
		{
			bytes[i] = subbox[bytes[i]];
		}
	}

	void AES256::shiftrows(uint8_t* bytes) const
	{
		uint8_t remember = bytes[1];
		bytes[1] = bytes[5];
		bytes[5] = bytes[9];
		bytes[9] = bytes[13];
		bytes[13] = remember;

		remember = bytes[10];
		bytes[10] = bytes[2];
		bytes[2] = remember;

		remember = bytes[3];
		bytes[3] = bytes[15];
		bytes[15] = bytes[11];
		bytes[11] = bytes[7];
		bytes[7] = remember;

		remember = bytes[14];
		bytes[14] = bytes[6];
		bytes[6] = remember;
	}

	uint8_t AES256::gmul(uint8_t x) const
	{
		return (x << 1) ^ (0x1B & (uint8_t)((int8_t)x >> 7));
	}

	void AES256::mixcolumns(uint8_t* bytes) const
	{
		for (uint8_t i = 0; i < 16; i += 4)
		{
			uint8_t cpy0 = bytes[i];
			uint8_t cpy1 = bytes[i + 1];
			uint8_t cpy2 = bytes[i + 2];
			uint8_t cpy3 = bytes[i + 3];

			uint8_t mul0 = gmul(bytes[i]);
			uint8_t mul1 = gmul(bytes[i + 1]);
			uint8_t mul2 = gmul(bytes[i + 2]);
			uint8_t mul3 = gmul(bytes[i + 3]);

			bytes[i] = mul0 ^ cpy3 ^ cpy2 ^ mul1 ^ cpy1;
			bytes[i + 1] = mul1 ^ cpy0 ^ cpy3 ^ mul2 ^ cpy2;
			bytes[i + 2] = mul2 ^ cpy1 ^ cpy0 ^ mul3 ^ cpy3;
			bytes[i + 3] = mul3 ^ cpy2 ^ cpy1 ^ mul0 ^ cpy0;
		}
	}
